
public class VariableTypes
{
	public static void main(String [] args)
	{
		//five variable types
		//Strings - Many Characters
		// char - one character
		//int - whole number 
		//double - decimals
		//boolean - true or false 
		String name = "Albert";
		char initial = 'W'; 
		int cats = -34;
		double money = 3.16;
		boolean psat = false;
		System.out.println(name+ " " + initial +" "+ cats +" "+ money + " " + psat);
		
		int math = 73;
		int english = 164;
		int science = 95; 
		
		int sum = math + english + science;
		int average = (sum*25/3);
		System.out.println("Erik's grades are " + math + " " + english + " " + science + " his average is " + average);
		int numA = 20;
		int numB = 6;
		int divide = numA % numB;
		System.out.println(divide);
		
		
	
	}
}
